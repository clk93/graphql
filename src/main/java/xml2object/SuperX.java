package xml2object;

public class SuperX {
    private String user;
    private String timestamp;
    private String startdate;
    private String runas;
    private String objectid;
    private String exportIds;
    private String integRunAsContextRole;

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public String getStartdate() {
        return startdate;
    }

    public void setStartdate(String startdate) {
        this.startdate = startdate;
    }

    public String getRunas() {
        return runas;
    }

    public void setRunas(String runas) {
        this.runas = runas;
    }

    public String getObjectid() {
        return objectid;
    }

    public void setObjectid(String objectid) {
        this.objectid = objectid;
    }

    public String getExportIds() {
        return exportIds;
    }

    public void setExportIds(String exportIds) {
        this.exportIds = exportIds;
    }

    public String getIntegRunAsContextRole() {
        return integRunAsContextRole;
    }

    public void setIntegRunAsContextRole(String integRunAsContextRole) {
        this.integRunAsContextRole = integRunAsContextRole;
    }
}
